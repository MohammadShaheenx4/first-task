                    <h3>Q: What should I do if my car overheats?</h3>
                    <p>A: If your car overheats, pull over safely and turn off the engine immediately. Do not attempt to open the radiator cap while the engine is hot, as it can cause burns. Allow the engine to cool down, then check the coolant level. If necessary, add coolant or water to the radiator. It's best to have your vehicle inspected by a professional to identify the cause of overheating.</p>
                </article>

                <article class="faq-item">
                    <h3>Q: How do I know when it's time to replace my tires?</h3>
                    <p>A: Tires should be replaced when the tread depth is below the recommended level (usually 2/32 of an inch). You can check tread depth using a tread depth gauge or a penny test. Additionally, look for signs of wear, such as cracks, bulges, or uneven tread wear. It's also advisable to replace tires every 6 years, regardless of mileage, due to aging rubber.</p>
